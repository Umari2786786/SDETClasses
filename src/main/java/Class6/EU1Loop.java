package Class6;

public class EU1Loop {
    public static void main(String[] args){
        int i=1; /*value set 1*/
        while (i<=15){/* will check if "int" value is less then the value on the left! */
            System.out.println("hello");
            i=i+1;/* will keep adding 1 (or what ever values on the left) to the "int" value above.. and then loop to i<=15 again */
        }
    }
}
