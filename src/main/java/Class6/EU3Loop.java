package Class6;

public class EU3Loop {
    public static void main(String[] args) {
        int i=1; /* the value i = 1 */

        while (i<=4){ /* will add 1 four time (total 4 times)*/
            System.out.println(i);
            i++; /* this adds 1 to i as it has ++ (increases the value of i). */
        }
    }
}
