package Class6;

public class EU4Loop {
    public static void main(String[] args) {
        int x=1;
        while (x<=5){ /* x value is less than 5 so it will print */
            if (x!=2){ /* the if condition will stop it from printing 2 when it has incremented once. */
            System.out.print(x+" ");}
            x++; /* it will add 1 to x and go to if! */
        }
    }
}
