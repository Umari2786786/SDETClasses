package Class7;

public class EU1NestedLoops {
    public static void main(String[] args) {
        for (int i=0;i<=5;i++){
            System.out.print("*");
        }
        System.out.println();
        for (int j=0;j<=10;j++){
            System.out.print("*");
        }
        System.out.println();
        for (int k=0;k<=5;k++){
            System.out.print("*");
        }
    }
}
