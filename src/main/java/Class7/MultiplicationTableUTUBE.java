package Class7;

public class MultiplicationTableUTUBE {
    public static void main(String[] args) {
        for (int i=1;i <=10; i++){ /* this line determines the number going across  */
            for (int j=1; j<=10; j++)/* this line determines the number going downwards  */
                System.out.print((i*j)+" ");
            /* this above prints the value of first line and 2nd - (i*j) is what multiples number by 1 2 3.... and leave space between numbers */
            System.out.println();/* this line makes you go to the line below  */
        }
    }
}
